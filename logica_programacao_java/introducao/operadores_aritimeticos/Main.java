package logica_programacao_java.introducao.operadores_aritimeticos;

public class Main {
    
    public static void main(String[] args) {
        double numA, numB, total;

        numA = 8;
        numB = 3;

        total = numA + numB;
        System.out.println(total);

        total = numA - numB;
        System.out.println(total);

        total = numA * numB;
        System.out.println(total);

        total = numA / numB;
        System.out.println(total);

        total = numA % numB;
        System.out.println(total);
    }
}
